    private void ChangeElemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeElemActionPerformed
        new OK(300, 300, "Изменить тип элемента масива", new I_Button() {
            @Override
            public void onPush() {
                try {
                    int type = types.get(Types.getSelectedIndex()).value();
                    Meta2GUI entity = (Meta2GUI) createByType(type);
                    if (entity.getErrors().getErrCount()!=0){
                        System.out.println(entity.getErrors().toString());
                        main.popup("Ошибка создания элемента ЧМИ");
                        return;
                        }
                    array.setElem(entity);
                    Elem.setText(entity.getFullTitle());
                    back.onEnter("Изменен тип элемента массива на "+types.get(Types.getSelectedIndex()).title());
                    } catch (Exception ee){
                        System.out.println("Ошибка: "+ee.toString());
                        array.setElem(null);
                }
            }
        });
    }//GEN-LAST:event_ChangeElemActionPerformed

