    public void openForm(WizardBaseView parentView0, Meta2Entity entity0){
        super.openForm(parentView0,entity0);
        resizeHeight(250);
        array = (Meta2GUIArray) entity;
        if (array.getElem()==null)
            Elem.setText("???");
        else
            Elem.setText(""+array.getElem().getFullTitle());
        DXY.setText(""+array.getDxy());
        Size.setText(""+array.getSize());
        Types.removeAll();
        types = Values.constMap().getGroupList("GUIType");
        types.sort(new Comparator<ConstValue>() {
            @Override
            public int compare(ConstValue o1, ConstValue o2) {
                return o1.title().compareTo(o2.title());
                }
            });
        for(ConstValue cc : types)
            Types.add(cc.title());
        for(int i=0;i<types.size();i++)
            if (types.get(i).value()==array.getElem().getMetaType()) {
                Types.select(i);
                break;
                }
        }
