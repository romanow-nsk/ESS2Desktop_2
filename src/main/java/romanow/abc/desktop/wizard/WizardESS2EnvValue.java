    private void SetMetaFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetMetaFileActionPerformed
        if (ScriptList.getSelectedIndex()==0){
            main.popup("Не выбран скрипт");
            return;
            }
        ESS2ScriptFile scriptFile = scripts.get(ScriptList.getSelectedIndex()-1);
        if (!(scriptFile.getScriptType()==Values.STCalcOnStart)) {
            main.popup("Недопустимый тип скрипта для переменных окружения");
            return;
            }
        if (!(scriptFile.isPreCompiled())) {
            main.popup("Требуется скрипт с предварительной компиляцией");
            return;
            }
        ScriptName.setText(scriptFile.getShortName());
        envValue.setScriptName(scriptFile.getShortName());
        oneUpdate("Изменено scriptName: "+envValue.getScriptName());
    }//GEN-LAST:event_SetMetaFileActionPerformed

