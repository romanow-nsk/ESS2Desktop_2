    private void selectChoice(){
        for(int i=0;i<scripts.size();i++){
            if (scripts.get(i).getShortName().equals(envValue.getScriptName())){
                ScriptList.select(i+1);
                return;
                }
            ScriptList.select(0);
            }
        }
    public WizardESS2EnvValue(ESSMetaPanel frame0, ESS2Entity entity0, I_Wizard back0) {
        super("Переменная окружения",frame0,entity0,back0);
        initComponents();
        setSize(750,220);
        envValue = (ESS2EnvValue) entity0;
        ScriptName.setText(envValue.getScriptName());
        scripts = panel.getArchitecture().getScripts();
        ScriptList.removeAll();
        ScriptList.add("...");
        for(ESS2ScriptFile scriptFile : scripts)
            ScriptList.add(scriptFile.toString());
        selectChoice();
    }

