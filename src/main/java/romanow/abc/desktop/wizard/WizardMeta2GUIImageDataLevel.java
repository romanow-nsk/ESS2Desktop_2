    private void selectCurrentImage(){
        long ownOid = elem.getPicture().getOid();
        if (ownOid==0){
            ImageList.select(0);
            return;
        }
        int idx2=1;
        for(Artifact artifact : artifacts){
            if (artifact.getOid()==ownOid){
                ImageList.select(idx2);
                break;
                }
            idx2++;
            }
        }
